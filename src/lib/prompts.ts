function getModeInstructions(mode: SessionMode, question?: string): string {
  switch (mode) {
    case 'master_question':
      return `【模式：禅师出题】
直接抛出一个禅题，一句话，不寒暄，不解释。

【出题风格要求】
- 用口语，像面对面跟人说话，不要书面语
- 标点只用逗号，句号，问号。不用破折号，省略号，引号，冒号，感叹号
- 非故事类问题控制在30字以内
- 故事类问题先用一两句话讲完故事，再问一个问题
- 释迦牟尼：温和从容，用温暖的比喻
- 文殊菩萨：极短，一句话，反问或悖论，像剑一样快
- 六祖慧能：大白话，像农民说话，粗粝直接，砍柴挑水吃饭那种词`;
    case 'daily':
      return `【模式：当日禅题】
今日禅题："${question || '万法归一，一归何处'}"
用你的方式把这个题抛给用户，一句话。`;
    case 'free':
      return `【模式：自由参禅】
一句话邀请用户说出心中所想。不超过一句话。`;
  }
}

