export function buildZenCardPrompt(messages: Array<{role: string; content: string}>, masterType: MasterType, level: string): string {
  const masterName = masterType === 'shakyamuni' ? '释迦牟尼' : masterType === 'manjushri' ? '文殊菩萨' : '六祖慧能';

  return `请根据以下参禅对话，生成一张禅卡的内容。

对话记录：
${messages.map(m => `${m.role === 'user' ? '参禅者' : masterName}：${m.content}`).join('\n')}

悟境等级：${level}

请以JSON格式返回：
{
  "core_question": "本次参禅的核心问题（一句话概括）",
  "core_answer": "参禅者最有灵性的一句回答",
  "master_comment": "${masterName}对本次参禅的一句评语（保持角色风格，10-30字）"
}

只返回JSON，不要其他内容。`;
}
